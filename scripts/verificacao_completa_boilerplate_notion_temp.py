import os
import json
from datetime import datetime
from notion_client import Client
from dotenv import load_dotenv

def verificar_conformidade_completa_notion():
    """Verifica√ß√£o completa de conformidade com boilerplate de todo o banco de dados Notion."""
    print("üîç VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE")
    print("=" * 70)
    
    # Carregar configura√ß√£o
    load_dotenv()
    
    notion_token = os.getenv("NOTION_TOKEN")
    database_id = os.getenv("DATABASE_ID")
    
    if not notion_token or not database_id:
        print("‚ùå Configura√ß√£o do Notion n√£o encontrada")
        return False
    
    # Inicializar cliente Notion
    notion = Client(auth=notion_token)
    
    try:
        print("üìä Buscando todas as p√°ginas do banco de dados...")
        
        # Buscar todas as p√°ginas (pode ser necess√°rio paginar)
        all_pages = []
        has_more = True
        start_cursor = None
        
        while has_more:
            query_params = {
                "database_id": database_id,
                "page_size": 100
            }
            
            if start_cursor:
                query_params["start_cursor"] = start_cursor
            
            response = notion.databases.query(**query_params)
            pages = response.get("results", [])
            all_pages.extend(pages)
            
            has_more = response.get("has_more", False)
            start_cursor = response.get("next_cursor")
            
            print(f"   üìÑ {len(all_pages)} p√°ginas carregadas...")
        
        print(f"‚úÖ Total de {len(all_pages)} p√°ginas encontradas no banco de dados")
        
        # Crit√©rios do boilerplate para verificar
        criterios_boilerplate = {
            "capa_titulo_data": "Capa com t√≠tulo e data",
            "resumo_executivo": "Resumo executivo",
            "dados_censo_escolar": "Dados do Censo Escolar 2024",
            "videos_youtube": "V√≠deos educativos do YouTube",
            "fontes_confiaveis": "Fontes confi√°veis",
            "conclusao": "Conclus√£o",
            "tags_apropriadas": "Tags apropriadas",
            "categoria_correta": "Categoria correta",
            "nivel_funcao": "N√≠vel de fun√ß√£o"
        }
        
        print(f"\nüîç Analisando conformidade com {len(criterios_boilerplate)} crit√©rios...")
        
        paginas_analisadas = []
        paginas_conformes = []
        paginas_nao_conformes = []
        estatisticas_criterios = {criterio: 0 for criterio in criterios_boilerplate.keys()}
        
        for i, page in enumerate(all_pages):
            page_id = page["id"]
            properties = page.get("properties", {})
            
            # Extrair t√≠tulo
            titulo = ""
            if "T√≠tulo" in properties:
                titulo_prop = properties["T√≠tulo"]
                if titulo_prop.get("title"):
                    titulo = titulo_prop["title"][0]["text"]["content"]
            
            # Se n√£o encontrou no T√≠tulo, tentar buscar no conte√∫do
            if not titulo:
                try:
                    blocks_response = notion.blocks.children.list(page_id)
                    blocks = blocks_response.get("results", [])
                    
                    for block in blocks:
                        if block.get("type") == "heading_1":
                            heading = block.get("heading_1", {})
                            rich_text = heading.get("rich_text", [])
                            if rich_text:
                                titulo = rich_text[0]["text"]["content"]
                                break
                except:
                    pass
            
            print(f"üìã Analisando ({i+1}/{len(all_pages)}): {titulo[:50]}...")
            
            try:
                # Buscar blocos da p√°gina
                blocks_response = notion.blocks.children.list(page_id)
                blocks = blocks_response.get("results", [])
                
                # Converter blocos para texto para an√°lise
                conteudo_texto = ""
                for block in blocks:
                    if block.get("type") in ["paragraph", "heading_1", "heading_2", "heading_3", "bulleted_list_item", "numbered_list_item"]:
                        rich_text = block.get(block["type"], {}).get("rich_text", [])
                        if rich_text:
                            texto_bloco = "".join([rt["text"]["content"] for rt in rich_text])
                            conteudo_texto += texto_bloco + "\n"
                
                # Verificar cada crit√©rio
                verificacoes = {}
                
                # 1. Capa com t√≠tulo e data
                verificacoes["capa_titulo_data"] = bool(titulo and len(titulo) > 5)
                
                # 2. Resumo executivo
                verificacoes["resumo_executivo"] = any(palavra in conteudo_texto.lower() 
                                                     for palavra in ["resumo", "executivo", "sum√°rio", "sumario"])
                
                # 3. Dados do Censo Escolar 2024
                verificacoes["dados_censo_escolar"] = any(palavra in conteudo_texto.lower() 
                                                        for palavra in ["censo escolar", "2024", "inep", "dados reais"])
                
                # 4. V√≠deos educativos do YouTube
                verificacoes["videos_youtube"] = "youtube" in conteudo_texto.lower() or "watch?v=" in conteudo_texto.lower()
                
                # 5. Fontes confi√°veis
                verificacoes["fontes_confiaveis"] = any(palavra in conteudo_texto.lower() 
                                                      for palavra in ["fonte:", "refer√™ncia", "bibliografia", "link", "mec", "inep"])
                
                # 6. Conclus√£o
                verificacoes["conclusao"] = any(palavra in conteudo_texto.lower() 
                                              for palavra in ["conclus√£o", "conclusao", "finalizando", "considera√ß√µes"])
                
                # 7. Tags apropriadas
                verificacoes["tags_apropriadas"] = "tags:" in conteudo_texto.lower() or "**tags**" in conteudo_texto.lower()
                
                # 8. Categoria correta
                verificacoes["categoria_correta"] = any(palavra in conteudo_texto.lower() 
                                                      for palavra in ["categoria:", "**categoria**", "gest√£o", "escolar", "educacional"])
                
                # 9. N√≠vel de fun√ß√£o
                verificacoes["nivel_funcao"] = any(palavra in conteudo_texto.lower() 
                                                 for palavra in ["n√≠vel:", "**n√≠vel**", "diretor", "coordenador", "gestor"])
                
                # Calcular pontua√ß√£o
                pontuacao = sum(1 for v in verificacoes.values() if v)
                total_criterios = len(verificacoes)
                percentual = (pontuacao / total_criterios) * 100
                
                # Determinar se est√° conforme (70% de conformidade)
                conforme = percentual >= 70
                
                # Atualizar estat√≠sticas dos crit√©rios
                for criterio, aprovado in verificacoes.items():
                    if aprovado:
                        estatisticas_criterios[criterio] += 1
                
                pagina_analisada = {
                    "page_id": page_id,
                    "titulo": titulo,
                    "verificacoes": verificacoes,
                    "pontuacao": pontuacao,
                    "total_criterios": total_criterios,
                    "percentual": percentual,
                    "conforme": conforme
                }
                
                paginas_analisadas.append(pagina_analisada)
                
                if conforme:
                    paginas_conformes.append(pagina_analisada)
                    print(f"   ‚úÖ CONFORME ({percentual:.1f}%) - {pontuacao}/{total_criterios} crit√©rios")
                else:
                    paginas_nao_conformes.append(pagina_analisada)
                    print(f"   ‚ùå N√ÉO CONFORME ({percentual:.1f}%) - {pontuacao}/{total_criterios} crit√©rios")
                
                # Progresso
                if (i + 1) % 20 == 0:
                    print(f"   üìä Progresso: {i + 1}/{len(all_pages)} p√°ginas analisadas")
                
            except Exception as e:
                print(f"   ‚ö†Ô∏è Erro ao analisar p√°gina {page_id}: {e}")
                # Adicionar p√°gina com erro
                pagina_erro = {
                    "page_id": page_id,
                    "titulo": titulo,
                    "erro": str(e),
                    "conforme": False
                }
                paginas_analisadas.append(pagina_erro)
                paginas_nao_conformes.append(pagina_erro)
        
        # Calcular estat√≠sticas gerais
        total_analisadas = len(paginas_analisadas)
        total_conformes = len(paginas_conformes)
        total_nao_conformes = len(paginas_nao_conformes)
        percentual_geral = (total_conformes / total_analisadas * 100) if total_analisadas > 0 else 0
        
        # Calcular estat√≠sticas por crit√©rio
        estatisticas_criterios_percentual = {}
        for criterio, total in estatisticas_criterios.items():
            estatisticas_criterios_percentual[criterio] = {
                "total": total,
                "percentual": (total / total_analisadas * 100) if total_analisadas > 0 else 0,
                "descricao": criterios_boilerplate[criterio]
            }
        
        # Salvar dados completos
        dados_completos = {
            "data_analise": datetime.now().isoformat(),
            "titulo": "VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE",
            "total_paginas_analisadas": total_analisadas,
            "total_conformes": total_conformes,
            "total_nao_conformes": total_nao_conformes,
            "percentual_geral_conformidade": percentual_geral,
            "criterios_boilerplate": criterios_boilerplate,
            "estatisticas_por_criterio": estatisticas_criterios_percentual,
            "paginas_analisadas": paginas_analisadas,
            "paginas_conformes": paginas_conformes,
            "paginas_nao_conformes": paginas_nao_conformes
        }
        
        with open("verificacao_completa_boilerplate_notion.json", "w", encoding="utf-8") as f:
            json.dump(dados_completos, f, indent=2, ensure_ascii=False, default=str)
        
        # Gerar relat√≥rio em markdown
        relatorio_md = f"""# VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE

**Data da Verifica√ß√£o:** {datetime.now().strftime('%d/%m/%Y √†s %H:%M')}

## üìä RESUMO EXECUTIVO

- **Total de P√°ginas Analisadas:** {total_analisadas}
- **P√°ginas Conformes:** {total_conformes}
- **P√°ginas N√£o Conformes:** {total_nao_conformes}
- **Percentual Geral de Conformidade:** {percentual_geral:.1f}%

## üìà ESTAT√çSTICAS POR CRIT√âRIO

"""
        
        for criterio, dados in estatisticas_criterios_percentual.items():
            relatorio_md += f"""
### {dados['descricao']}
- **Total:** {dados['total']}/{total_analisadas}
- **Percentual:** {dados['percentual']:.1f}%
"""
        
        relatorio_md += f"""
## ‚úÖ P√ÅGINAS CONFORMES ({total_conformes})

"""
        
        for i, pagina in enumerate(paginas_conformes[:20], 1):
            if "erro" not in pagina:
                relatorio_md += f"{i}. {pagina['titulo'][:60]}... ({pagina['percentual']:.1f}%)\n"
            else:
                relatorio_md += f"{i}. {pagina['titulo'][:60]}... (ERRO)\n"
        
        if len(paginas_conformes) > 20:
            relatorio_md += f"... e mais {len(paginas_conformes) - 20} p√°ginas conformes\n"
        
        relatorio_md += f"""
## ‚ùå P√ÅGINAS N√ÉO CONFORMES ({total_nao_conformes})

"""
        
        for i, pagina in enumerate(paginas_nao_conformes[:20], 1):
            if "erro" not in pagina:
                relatorio_md += f"{i}. {pagina['titulo'][:60]}... ({pagina['percentual']:.1f}%)\n"
            else:
                relatorio_md += f"{i}. {pagina['titulo'][:60]}... (ERRO)\n"
        
        if len(paginas_nao_conformes) > 20:
            relatorio_md += f"... e mais {len(paginas_nao_conformes) - 20} p√°ginas n√£o conformes\n"
        
        relatorio_md += f"""
## üéØ RECOMENDA√á√ïES

1. **P√°ginas N√£o Conformes:** {total_nao_conformes} p√°ginas precisam de corre√ß√£o
2. **Crit√©rios Mais Cr√≠ticos:** Verificar crit√©rios com menor percentual de conformidade
3. **Processo de Corre√ß√£o:** Implementar corre√ß√£o autom√°tica para p√°ginas n√£o conformes
4. **Monitoramento Cont√≠nuo:** Estabelecer verifica√ß√£o peri√≥dica de conformidade

---
*Relat√≥rio gerado automaticamente em {datetime.now().strftime('%d/%m/%Y √†s %H:%M')}*
"""
        
        # Salvar relat√≥rio em markdown
        with open("relatorio_verificacao_completa_boilerplate.md", "w", encoding="utf-8") as f:
            f.write(relatorio_md)
        
        print(f"\nüìä RESUMO FINAL:")
        print(f"   üìÑ Total de p√°ginas analisadas: {total_analisadas}")
        print(f"   ‚úÖ P√°ginas conformes: {total_conformes}")
        print(f"   ‚ùå P√°ginas n√£o conformes: {total_nao_conformes}")
        print(f"   üìä Percentual geral de conformidade: {percentual_geral:.1f}%")
        print(f"   üíæ Dados salvos: verificacao_completa_boilerplate_notion.json")
        print(f"   üìù Relat√≥rio: relatorio_verificacao_completa_boilerplate.md")
        
        print(f"\nüìà CONFORMIDADE POR CRIT√âRIO:")
        for criterio, dados in estatisticas_criterios_percentual.items():
            print(f"   {dados['descricao']}: {dados['percentual']:.1f}% ({dados['total']}/{total_analisadas})")
        
        if paginas_nao_conformes:
            print(f"\n‚ùå PRINCIPAIS P√ÅGINAS N√ÉO CONFORMES:")
            for i, pagina in enumerate(paginas_nao_conformes[:10], 1):
                if "erro" not in pagina:
                    print(f"   {i}. {pagina['titulo'][:60]}... ({pagina['percentual']:.1f}%)")
                else:
                    print(f"   {i}. {pagina['titulo'][:60]}... (ERRO)")
            if len(paginas_nao_conformes) > 10:
                print(f"   ... e mais {len(paginas_nao_conformes) - 10} p√°ginas n√£o conformes")
        
        return True
        
    except Exception as e:
        print(f"‚ùå Erro na verifica√ß√£o completa: {e}")
        return False

def main():
    print("üîç VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE")
    print("======================================================================")
