import os
import json
from datetime import datetime
from notion_client import Client
from dotenv import load_dotenv

def verificar_conformidade_boilerplate_completa():
    """Verifica√ß√£o completa se todo o processo seguiu rigorosamente o boilerplate."""
    print("üîç VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE")
    print("=" * 70)
    
    # Carregar configura√ß√£o
    load_dotenv()
    
    notion_token = os.getenv("NOTION_TOKEN")
    
    if not notion_token:
        print("‚ùå Configura√ß√£o do Notion n√£o encontrada")
        return False
    
    # Inicializar cliente Notion
    notion = Client(auth=notion_token)
    
    try:
        # Carregar dados de todas as corre√ß√µes
        dados_correcoes = {}
        
        # Carregar dados da primeira rodada
        try:
            with open("correcao_completa_boilerplate_100.json", "r", encoding="utf-8") as f:
                dados_correcoes["primeira_rodada"] = json.load(f)
            print("‚úÖ Dados da primeira rodada carregados")
        except FileNotFoundError:
            print("‚ö†Ô∏è Arquivo da primeira rodada n√£o encontrado")
            dados_correcoes["primeira_rodada"] = None
        
        # Carregar dados da segunda rodada
        try:
            with open("segunda_rodada_correcao_100.json", "r", encoding="utf-8") as f:
                dados_correcoes["segunda_rodada"] = json.load(f)
            print("‚úÖ Dados da segunda rodada carregados")
        except FileNotFoundError:
            print("‚ö†Ô∏è Arquivo da segunda rodada n√£o encontrado")
            dados_correcoes["segunda_rodada"] = None
        
        # Carregar dados da corre√ß√£o final
        try:
            with open("correcao_final_paginas_reprovadas.json", "r", encoding="utf-8") as f:
                dados_correcoes["correcao_final"] = json.load(f)
            print("‚úÖ Dados da corre√ß√£o final carregados")
        except FileNotFoundError:
            print("‚ö†Ô∏è Arquivo da corre√ß√£o final n√£o encontrado")
            dados_correcoes["correcao_final"] = None
        
        # Carregar dados da an√°lise de sincroniza√ß√£o
        try:
            with open("analise_completa_sincronizacao_notion.json", "r", encoding="utf-8") as f:
                dados_correcoes["sincronizacao"] = json.load(f)
            print("‚úÖ Dados da sincroniza√ß√£o carregados")
        except FileNotFoundError:
            print("‚ö†Ô∏è Arquivo da sincroniza√ß√£o n√£o encontrado")
            dados_correcoes["sincronizacao"] = None
        
        print(f"\nüìä VERIFICA√á√ÉO DE CONFORMIDADE COM BOILERPLATE:")
        print(f"   üîÑ Primeira rodada: {'‚úÖ' if dados_correcoes['primeira_rodada'] else '‚ùå'}")
        print(f"   üîÑ Segunda rodada: {'‚úÖ' if dados_correcoes['segunda_rodada'] else '‚ùå'}")
        print(f"   üîÑ Corre√ß√£o final: {'‚úÖ' if dados_correcoes['correcao_final'] else '‚ùå'}")
        print(f"   üîÑ Sincroniza√ß√£o: {'‚úÖ' if dados_correcoes['sincronizacao'] else '‚ùå'}")
        
        # Consolidar todas as p√°ginas processadas
        todas_paginas_processadas = set()
        
        if dados_correcoes["primeira_rodada"]:
            paginas_primeira = [p["page_id"] for p in dados_correcoes["primeira_rodada"]["paginas_corrigidas"]]
            todas_paginas_processadas.update(paginas_primeira)
            print(f"   üìÑ Primeira rodada: {len(paginas_primeira)} p√°ginas")
        
        if dados_correcoes["segunda_rodada"]:
            paginas_segunda = [p["page_id"] for p in dados_correcoes["segunda_rodada"]["paginas_corrigidas_segunda_rodada"]]
            todas_paginas_processadas.update(paginas_segunda)
            print(f"   üìÑ Segunda rodada: {len(paginas_segunda)} p√°ginas")
        
        if dados_correcoes["correcao_final"]:
            paginas_final = [p["page_id"] for p in dados_correcoes["correcao_final"]["paginas_corrigidas_final"]]
            todas_paginas_processadas.update(paginas_final)
            print(f"   üìÑ Corre√ß√£o final: {len(paginas_final)} p√°ginas")
        
        print(f"\nüìä TOTAL DE P√ÅGINAS PROCESSADAS: {len(todas_paginas_processadas)}")
        
        # Verificar conformidade com boilerplate em cada p√°gina
        print(f"\nüîç VERIFICANDO CONFORMIDADE COM BOILERPLATE...")
        
        paginas_conformes = []
        paginas_nao_conformes = []
        paginas_com_erro = []
        
        # Crit√©rios do boilerplate (9 elementos obrigat√≥rios)
        criterios_boilerplate = {
            "censo_escolar": "Dados do Censo Escolar 2024",
            "videos": "V√≠deos educativos do YouTube",
            "fontes": "Fontes confi√°veis e refer√™ncias",
            "resumo_executivo": "Resumo executivo",
            "tags": "Tags e categoriza√ß√£o",
            "conclusao": "Conclus√£o estruturada",
            "dados_reais": "Dados reais e estat√≠sticas",
            "metodologia": "Metodologia e aplicabilidade",
            "qualidade": "Padr√µes de qualidade educacional"
        }
        
        for i, page_id in enumerate(todas_paginas_processadas):
            print(f"   üîç Verificando p√°gina {i+1}/{len(todas_paginas_processadas)}: {page_id[:8]}...")
            
            try:
                # Buscar p√°gina no Notion
                page = notion.pages.retrieve(page_id)
                
                # Buscar blocos da p√°gina
                blocks_response = notion.blocks.children.list(page_id)
                blocks = blocks_response.get("results", [])
                
                # Converter blocos para texto para an√°lise
                conteudo_texto = ""
                for block in blocks:
                    if block.get("type") in ["paragraph", "heading_1", "heading_2", "heading_3", "bulleted_list_item", "numbered_list_item"]:
                        rich_text = block.get(block["type"], {}).get("rich_text", [])
                        if rich_text:
                            texto_bloco = "".join([rt["text"]["content"] for rt in rich_text])
                            conteudo_texto += texto_bloco + "\n"
                
                # Verificar cada crit√©rio do boilerplate
                conformidade_criterios = {}
                
                # 1. Censo Escolar 2024
                conformidade_criterios["censo_escolar"] = any(palavra in conteudo_texto.lower() for palavra in ["censo escolar", "2024", "inep", "estat√≠sticas nacionais"])
                
                # 2. V√≠deos educativos
                conformidade_criterios["videos"] = "youtube" in conteudo_texto.lower() and "v√≠deos" in conteudo_texto.lower()
                
                # 3. Fontes confi√°veis
                conformidade_criterios["fontes"] = any(palavra in conteudo_texto.lower() for palavra in ["fonte:", "refer√™ncia", "mec", "inep", "fnde"])
                
                # 4. Resumo executivo
                conformidade_criterios["resumo_executivo"] = any(palavra in conteudo_texto.lower() for palavra in ["resumo", "executivo", "sum√°rio", "objetivos"])
                
                # 5. Tags e categoriza√ß√£o
                conformidade_criterios["tags"] = "tags:" in conteudo_texto.lower() or "**tags**" in conteudo_texto.lower() or "categoria:" in conteudo_texto.lower()
                
                # 6. Conclus√£o
                conformidade_criterios["conclusao"] = any(palavra in conteudo_texto.lower() for palavra in ["conclus√£o", "conclusao", "finalizando", "pr√≥ximos passos"])
                
                # 7. Dados reais
                conformidade_criterios["dados_reais"] = any(palavra in conteudo_texto.lower() for palavra in ["dados", "estat√≠sticas", "indicadores", "m√©tricas"])
                
                # 8. Metodologia
                conformidade_criterios["metodologia"] = any(palavra in conteudo_texto.lower() for palavra in ["metodologia", "aplicabilidade", "implementa√ß√£o", "processo"])
                
                # 9. Qualidade educacional
                conformidade_criterios["qualidade"] = any(palavra in conteudo_texto.lower() for palavra in ["qualidade", "educacional", "pedag√≥gico", "gest√£o"])
                
                # Calcular pontua√ß√£o
                total_criterios = len(conformidade_criterios)
                criterios_atingidos = sum(conformidade_criterios.values())
                percentual_conformidade = (criterios_atingidos / total_criterios) * 100
                
                # Determinar status
                if percentual_conformidade >= 80:
                    status_conformidade = "CONFORME"
                    paginas_conformes.append({
                        "page_id": page_id,
                        "titulo": page.get("properties", {}).get("title", {}).get("title", [{}])[0].get("text", {}).get("content", "Sem t√≠tulo"),
                        "percentual_conformidade": percentual_conformidade,
                        "criterios_atingidos": criterios_atingidos,
                        "criterios_faltando": [k for k, v in conformidade_criterios.items() if not v],
                        "conformidade_detalhada": conformidade_criterios
                    })
                    print(f"      ‚úÖ {status_conformidade} ({percentual_conformidade:.1f}%) - {criterios_atingidos}/{total_criterios} crit√©rios")
                else:
                    status_conformidade = "N√ÉO CONFORME"
                    paginas_nao_conformes.append({
                        "page_id": page_id,
                        "titulo": page.get("properties", {}).get("title", {}).get("title", [{}])[0].get("text", {}).get("content", "Sem t√≠tulo"),
                        "percentual_conformidade": percentual_conformidade,
                        "criterios_atingidos": criterios_atingidos,
                        "criterios_faltando": [k for k, v in conformidade_criterios.items() if not v],
                        "conformidade_detalhada": conformidade_criterios
                    })
                    print(f"      ‚ùå {status_conformidade} ({percentual_conformidade:.1f}%) - {criterios_atingidos}/{total_criterios} crit√©rios")
                
            except Exception as e:
                print(f"      ‚ö†Ô∏è ERRO: {e}")
                paginas_com_erro.append({
                    "page_id": page_id,
                    "erro": str(e)
                })
        
        # Calcular estat√≠sticas finais
        total_verificadas = len(todas_paginas_processadas)
        total_conformes = len(paginas_conformes)
        total_nao_conformes = len(paginas_nao_conformes)
        total_com_erro = len(paginas_com_erro)
        percentual_conformidade_geral = (total_conformes / total_verificadas * 100) if total_verificadas > 0 else 0
        
        print(f"\nüìä RESULTADOS DA VERIFICA√á√ÉO DE CONFORMIDADE COM BOILERPLATE:")
        print(f"   üìÑ Total de p√°ginas verificadas: {total_verificadas}")
        print(f"   ‚úÖ P√°ginas conformes: {total_conformes}")
        print(f"   ‚ùå P√°ginas n√£o conformes: {total_nao_conformes}")
        print(f"   ‚ö†Ô∏è P√°ginas com erro: {total_com_erro}")
        print(f"   üìä Percentual de conformidade geral: {percentual_conformidade_geral:.1f}%")
        
        # An√°lise detalhada dos crit√©rios
        print(f"\nüìã AN√ÅLISE DETALHADA DOS CRIT√âRIOS DO BOILERPLATE:")
        
        # Contar conformidade por crit√©rio
        contador_criterios = {}
        for pagina in paginas_conformes + paginas_nao_conformes:
            for criterio, atingido in pagina["conformidade_detalhada"].items():
                if criterio not in contador_criterios:
                    contador_criterios[criterio] = {"atingido": 0, "total": 0}
                contador_criterios[criterio]["total"] += 1
                if atingido:
                    contador_criterios[criterio]["atingido"] += 1
        
        for criterio, dados in contador_criterios.items():
            percentual_criterio = (dados["atingido"] / dados["total"] * 100) if dados["total"] > 0 else 0
            nome_criterio = criterios_boilerplate.get(criterio, criterio.replace("_", " ").title())
            status_criterio = "‚úÖ" if percentual_criterio >= 80 else "‚ùå"
            print(f"   {status_criterio} {nome_criterio}: {dados['atingido']}/{dados['total']} ({percentual_criterio:.1f}%)")
        
        # Salvar dados da verifica√ß√£o
        dados_verificacao = {
            "data_verificacao": datetime.now().isoformat(),
            "titulo": "VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE",
            "total_paginas_verificadas": total_verificadas,
            "total_conformes": total_conformes,
            "total_nao_conformes": total_nao_conformes,
            "total_com_erro": total_com_erro,
            "percentual_conformidade_geral": percentual_conformidade_geral,
            "criterios_boilerplate": criterios_boilerplate,
            "analise_criterios": contador_criterios,
            "paginas_conformes": paginas_conformes,
            "paginas_nao_conformes": paginas_nao_conformes,
            "paginas_com_erro": paginas_com_erro
        }
        
        with open("verificacao_boilerplate_completa.json", "w", encoding="utf-8") as f:
            json.dump(dados_verificacao, f, indent=2, ensure_ascii=False, default=str)
        
        # Determinar se o processo seguiu o boilerplate
        if percentual_conformidade_geral >= 80:
            print(f"\n‚úÖ RESULTADO FINAL: O PROCESSO SEGUIU RIGOROSAMENTE O BOILERPLATE!")
            print(f"   üìä {percentual_conformidade_geral:.1f}% de conformidade geral")
            print(f"   ‚úÖ {total_conformes} p√°ginas conformes com boilerplate")
            print(f"   üéØ Padr√µes de qualidade educacional mantidos")
            return True
        else:
            print(f"\n‚ùå RESULTADO FINAL: O PROCESSO N√ÉO SEGUIU COMPLETAMENTE O BOILERPLATE!")
            print(f"   üìä {percentual_conformidade_geral:.1f}% de conformidade geral")
            print(f"   ‚ùå {total_nao_conformes} p√°ginas n√£o conformes")
            print(f"   ‚ö†Ô∏è Necess√°rio ajustes para atingir 80% de conformidade")
            return False
        
    except Exception as e:
        print(f"‚ùå Erro na verifica√ß√£o de conformidade: {e}")
        return False

def main():
    print("üîç VERIFICA√á√ÉO COMPLETA DE CONFORMIDADE COM BOILERPLATE")
